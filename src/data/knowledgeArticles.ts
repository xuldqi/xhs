export function getArticlesByCategory(category: string): Article[] {
  return knowledgeArticles.filter(article => article.category === category)
}

// 获取精选文章
export function getFeaturedArticles(): Article[] {
  return knowledgeArticles.filter(article => article.featured)
}

// 根据slug获取文章
export function getArticleBySlug(slug: string): Article | undefined {
  return knowledgeArticles.find(article => article.slug === slug)
}

// 获取相关文章
export function getRelatedArticles(articleId: string, limit: number = 3): Article[] {
  const article = knowledgeArticles.find(a => a.id === articleId)
  if (!article) return []
  
  return knowledgeArticles
    .filter(a => a.id !== articleId && a.category === article.category)
    .slice(0, limit)
}

