// 按类型获取情报
export function getIntelligenceByType(type: string): IntelligencePost[] {
  return intelligencePosts.filter(post => post.type === type)
}

// 获取最新情报
export function getLatestIntelligence(limit: number = 5): IntelligencePost[] {
  return intelligencePosts
    .sort((a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime())
    .slice(0, limit)
}

// 根据slug获取情报
export function getIntelligenceBySlug(slug: string): IntelligencePost | undefined {
  return intelligencePosts.find(post => post.slug === slug)
}

